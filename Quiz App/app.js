let mainCard = document.getElementById("card");
let btn = document.querySelector("button");
let alertBox = document.querySelector(".alert-box");
let altImg = document.querySelector("#alt-img");
let WatchBrand = document.querySelector("#SelectBrand");
let WatchtVariant = document.querySelector("#SelectVariant");
let navLogo = document.getElementById("nav-logo");
mainCard.classList.add("color-none");

let selectedBrand;
let selectedItem;
let imgsrc;

let allProducts = document.getElementById("all-products");

//! showing all products on screen
function showallProducts() {
  for (let brands in watches) {
    for (let variants in watches[brands]) {
      // console.log(watches[brands][variants].img);
      let variantsImg = watches[brands][variants];
      allProducts.innerHTML += `
      <div id="mainHero">
        <img src="${variantsImg.img}" width="250" height="auto">
        <h3>${variantsImg.name}</h3>
      </div>`;
    }
  }
}

//! adding brand names to option
for (let brands in watches) {
  WatchBrand.innerHTML += `
      <option value="${brands}">${brands}</option>;
    `;
}

//! showing Brands when select from option of brands like rolex watches if user select (ROLEX)
function selectBrand() {
  // console.log(event.target.value);
  selectedBrand = event.target.value;
  WatchtVariant.innerHTML = `<option selected disabled value="">Select Variant</option>`;
  for (let variants in watches[selectedBrand]) {
    // console.log(watches[selectedBrand][variants].name);
    WatchtVariant.innerHTML += `
       <option value="${watches[selectedBrand][variants].name}">${watches[selectedBrand][variants].name}</option>;
    `;
  }

  if (selectedBrand) {
    allProducts.innerHTML = "";
    mainCard.innerHTML = "";
    mainCard.classList.add("color-none");
    allProducts.innerHTML += `<h1>${selectedBrand} Watches</h1>`;
    for (let brands in watches[selectedBrand]) {
      // console.log(watches[selectedBrand][brands]);
      let brandImg = watches[selectedBrand][brands];
      allProducts.innerHTML += `
        <div id="mainHero">
          <img src="${brandImg.img}" width="250" height="auto">
          <h3>${brandImg.name}</h3>
        </div>`;
    }
  }
}

//! showing Variants of watch brand like rolex explorer when select from option of variations
function selectVariant() {
  selectedItem = event.target.value;
  for (let keys in watches[selectedBrand]) {
    if (watches[selectedBrand][keys].name == selectedItem) {
      mainCard.classList.remove("color-none");
      mainCard.innerHTML = `
        <h1 class="card-title">${watches[selectedBrand][keys].name}</h1>
        <img id="close-icon" src="${"close.png"}">
        <img id="Watch-img" src="${watches[selectedBrand][keys].img}">
        <h2 class="card-price">Price : ${
          watches[selectedBrand][keys].price
        }</h2>
        <p class="card-caption">${watches[selectedBrand][keys].caption}</p>
        `;
      allProducts.innerHTML = "";

      let closeCard = document.querySelector("#close-icon");
      closeCard.addEventListener("click", function (e) {
        mainCard.innerHTML = "";
        mainCard.classList.add("color-none");
        selectBrand();
        selectVariant();
        WatchBrand.innerHTML = `
        <option selected disabled value="">Select Brand</option>
        `;
        for (let brands in watches) {
          WatchBrand.innerHTML += `
                <option value="${brands}">${brands}</option>;
              `;
        }

        //! ---------backup of all products
        showallProducts();
      });
    }
  }
}

//! showing all products when click on home(logo)
navLogo.addEventListener("click", function () {
  allProducts.innerHTML = "";
  mainCard.innerHTML = "";
  mainCard.classList.add("color-none");
  WatchBrand.innerHTML = `<option selected disabled value="">Select Brand</option>`;
  for (let brands in watches) {
    WatchBrand.innerHTML += `
                <option value="${brands}">${brands}</option>;
              `;
  }
  WatchtVariant.innerHTML = `<option selected disabled value="">Select Variant</option>`;
  for (let variants in watches[selectedBrand]) {
    // console.log(watches[selectedBrand][variants].name);
    WatchtVariant.innerHTML += `
             <option value="${watches[selectedBrand][variants].name}">${watches[selectedBrand][variants].name}</option>;
          `;
  }
  showallProducts();
});

//! show card when click on image of watch
